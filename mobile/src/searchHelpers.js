export function normalizeCategoryLabel(value) {
  return String(value || '')
    .toLowerCase()
    .normalize('NFKD')
    .replace(/[\u0300-\u036f]/g, '')
    .replace(/[^a-z0-9]+/g, ' ')
    .trim();
}

export function resolveMainCategoryLabel(category) {
  const normalized = normalizeCategoryLabel(category);

  for (const group of CATEGORY_GROUP_RULES) {
    if (group.patterns.some((pattern) => normalized.includes(normalizeCategoryLabel(pattern)))) {
      return group.mainCategory;
    }
  }

  if (normalized === 'lebensmittel') {
    return 'Weitere Lebensmittel';
  }

  if (normalized === 'getraenke' || normalized === 'getranke') {
    return 'Weitere Getränke';
  }

  return category || 'Weitere Kategorien';
}

export function buildCategoryGroups(categories = []) {
  if ((categories || []).some((item) => item && typeof item === 'object')) {
    return (categories || [])
      .filter((item) => item && typeof item === 'object')
      .map((category) => ({
        mainCategory: category.mainCategoryLabel || resolveMainCategoryLabel(category.mainCategoryLabel || ''),
        subcategories: [...new Set(
          (category.subcategories || [])
            .map((subcategory) => subcategory?.subcategoryLabel)
            .filter(Boolean)
        )].sort((left, right) => left.localeCompare(right, 'de')),
      }))
      .sort((left, right) => left.mainCategory.localeCompare(right.mainCategory, 'de'));
  }

  const grouped = new Map();

  for (const category of categories) {
    const mainCategory = resolveMainCategoryLabel(category);

    if (!grouped.has(mainCategory)) {
      grouped.set(mainCategory, []);
    }

    grouped.get(mainCategory).push(category);
  }

  return [...grouped.entries()]
    .map(([mainCategory, subcategories]) => ({
      mainCategory,
      subcategories: [...new Set(subcategories)].sort((left, right) => left.localeCompare(right, 'de')),
    }))
    .sort((left, right) => left.mainCategory.localeCompare(right.mainCategory, 'de'));
}

export function getOfferCategoryLabel(offer) {
  return offer?.displayCategory || offer?.categorySecondary || offer?.categoryPrimary || 'ohne Kategorie';
}

export function formatCurrency(amount, currency = 'EUR') {
  if (amount === null || amount === undefined || Number.isNaN(Number(amount))) {
    return '-';
  }

  return `${Number(amount).toFixed(2)} ${currency}`;
}
